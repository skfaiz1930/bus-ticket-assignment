const mongoose = require("mongoose");
const AdminModel = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    unique: true,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
});
module.exports = Admin = mongoose.model("admin", AdminModel);
